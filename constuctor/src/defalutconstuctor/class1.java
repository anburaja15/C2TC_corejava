package defalutconstuctor;

public class class1 {
 //default Constructor
	class1()
	{
		System.out.println("default constructer");		
	}
	//parameterized constructor
	class1(int a)
	{
     System.out.println("A"+a);
	}
	class1(int b,String c){
		System.out.println("b"+b+"c"+c);	
	}
	class1(String d){
		System.out.println("d"+d);
	}

}
